#include "libft.h"

size_t	ft_strlcat(char *dst, const char *src, size_t size)
{
	size_t	d_len;
	size_t	s_len;
	size_t	i;

	d_len = 0;
	s_len = 0;
	while (d_len < size && dst[d_len])
		d_len++;
	s_len = ft_strlen(src);
	if (d_len == size)
		return (d_len + s_len);
	i = 0;
	while (i < size - d_len - 1 && src[i])
	{
		dst[d_len + i] = src[i];
		i++;
	}
	dst[d_len + i] = '\0';
	return (d_len + s_len);
}
